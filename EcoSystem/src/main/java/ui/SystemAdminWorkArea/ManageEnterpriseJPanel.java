    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        String name = nameJTextField.getText().trim().toLowerCase(); // 获取搜索框的内容
        Network selectedNetwork = (Network) networkJComboBox.getSelectedItem(); // 获取选中的 Network
        Enterprise.EnterpriseType selectedType = (Enterprise.EnterpriseType) enterpriseTypeJComboBox.getSelectedItem(); // 获取选中的 Enterprise Type

        if (selectedNetwork == null || selectedType == null) {
            JOptionPane.showMessageDialog(null, "Please select a valid network and enterprise type from ComboBox!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();
        model.setRowCount(0); // 清空表格数据

        // 遍历企业列表，根据条件筛选
        for (Enterprise enterprise : selectedNetwork.getEnterpriseDirectory().getEnterpriseList()) {
            boolean matchesName = name.isEmpty() || enterprise.getName().toLowerCase().contains(name.toLowerCase());
            boolean matchesType = enterprise.getEnterpriseType() == selectedType;

            if (matchesName && matchesType) { // 符合条件
                Object[] row = new Object[3];
                row[0] = enterprise.getName();
                row[1] = selectedNetwork.getName();
                row[2] = enterprise.getEnterpriseType().getValue();
                model.addRow(row);
            }
        }

        // 如果没有找到匹配的企业，提示用户
        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "No enterprises found matching the criteria!", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

