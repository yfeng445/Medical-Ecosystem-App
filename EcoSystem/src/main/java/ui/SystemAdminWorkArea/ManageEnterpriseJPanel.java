    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = enterpriseJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to view details.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String enterpriseName = (String) enterpriseJTable.getValueAt(selectedRow, 0);
        String networkName = (String) enterpriseJTable.getValueAt(selectedRow, 1);

        Network selectedNetwork = system.get(networkName);
        Enterprise selectedEnterprise = selectedNetwork.getEnterpriseDirectory().getEnterprise(enterpriseName);

        EnterpriseDetailsJPanel enterpriseDetailsJPanel = new EnterpriseDetailsJPanel(userProcessContainer, selectedEnterprise);
        userProcessContainer.add("EnterpriseDetailsJPanel", enterpriseDetailsJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnViewActionPerformed

