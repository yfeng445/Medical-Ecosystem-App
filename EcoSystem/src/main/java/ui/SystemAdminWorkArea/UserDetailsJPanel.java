        // TODO add your handling code here:
        parentPanel.remove(this);
    

        Component[] componentArray = parentPanel.getComponents();
        Component component = componentArray[componentArray.length - 1];
        if (component instanceof ManageEnterpriseJPanel) {
            ManageEnterpriseJPanel manageEnterpriseJPanel = (ManageEnterpriseJPanel) component;
            manageEnterpriseJPanel.populateTable();
        }

        CardLayout layout = (CardLayout) parentPanel.getLayout();
        layout.previous(parentPanel);
