    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

        if (txtUsername.getText().isBlank() || 
            txtPassword.getText().trim().isEmpty() || 
            txtName.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Fields cannot be empty.", "Warning", JOptionPane.ERROR_MESSAGE);
            return;
        }

        userAccount.getEmployee().setName(txtName.getText().trim());
        userAccount.setPassword(txtPassword.getText().trim());
        userAccount.setUsername(txtUsername.getText().trim());
        
        JOptionPane.showMessageDialog(null, "Enterprise details updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

        setFieldsEditable(false); 
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true); 
    }//GEN-LAST:event_btnSaveActionPerformed

